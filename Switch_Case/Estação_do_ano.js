let estação = 2;

switch (estação) {
    case 1:
        console.log("É verão");
        break;
    case 2:
        console.log("É outono");
        break;
    case 3:
        console.log("É inverno");
        break;
    case 4:
        console.log("É primavera");
        break;
    default:
        console.log("Estação invalida");
        break;
}